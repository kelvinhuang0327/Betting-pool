class TestBssSafetyGate:
    def test_gate_locked_when_calibrated_bss_negative(self):
        """When calibrated BSS < 0, patch_gate_eligible must be False."""
        rows = _make_rows(120, model_prob=0.8, market_prob=0.52)
        report = run_calibration_repair(rows, methods=["identity"], n_splits=3)
        if report.classification == CalibrationClassification.INSUFFICIENT_DATA:
            pytest.skip("Not enough data")
        if report.calibrated_bss_overall is not None and report.calibrated_bss_overall < 0:
            assert report.patch_gate_eligible is False

    def test_gate_allows_metric_repair_task(self):
        """
        The BSS Safety Gate must allow task_kind='metric_repair' even
        when BSS is negative (it is in the allowed prefixes list).
        """
        rows = _make_rows(120)
        report = run_calibration_repair(rows, methods=["identity"], n_splits=3)
        if report.classification == CalibrationClassification.INSUFFICIENT_DATA:
            pytest.skip("Not enough data")
        gate = report.bss_gate
        assert gate.get("task_kind") == "metric_repair"
        assert gate.get("allowed") is True, (
            "metric_repair must always be ALLOWED by the BSS Safety Gate"
        )

    def test_bss_gate_bss_negative_flag_matches_calibrated_bss(self):
        """gate.bss_negative must match (calibrated_bss_overall < 0)."""
        rows = _make_rows(120)
        report = run_calibration_repair(rows, methods=["identity"], n_splits=3)
        if report.classification == CalibrationClassification.INSUFFICIENT_DATA:
            pytest.skip("Not enough data")
        gate = report.bss_gate
        expected = (
            report.calibrated_bss_overall is not None
            and report.calibrated_bss_overall < 0.0
        )
        assert gate.get("bss_negative") == expected


