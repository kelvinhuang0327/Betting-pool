class TestMarketBlend:
    def test_alpha_0_returns_pure_market(self):
        model_probs = [0.6, 0.7, 0.5]
        market_probs = [0.52, 0.48, 0.51]
        result = _apply_market_blend(model_probs, market_probs, alpha=0.0)
        for r, m in zip(result, market_probs):
            assert r == pytest.approx(m, abs=1e-10)

    def test_alpha_1_returns_pure_model(self):
        model_probs = [0.6, 0.7, 0.5]
        market_probs = [0.52, 0.48, 0.51]
        result = _apply_market_blend(model_probs, market_probs, alpha=1.0)
        for r, m in zip(result, model_probs):
            assert r == pytest.approx(m, abs=1e-10)

    def test_alpha_half_is_average(self):
        model_probs = [0.6, 0.4]
        market_probs = [0.52, 0.48]
        result = _apply_market_blend(model_probs, market_probs, alpha=0.5)
        expected = [(m + mk) / 2 for m, mk in zip(model_probs, market_probs)]
        for r, e in zip(result, expected):
            assert r == pytest.approx(e, abs=1e-10)


