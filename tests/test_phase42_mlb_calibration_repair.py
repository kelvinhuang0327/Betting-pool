def _make_row(
    game_date: str,
    model_prob: float,
    market_prob: float,
    home_win: int,
    game_id: str = "",
) -> PredictionRow:
    """Build a minimal PredictionRow for testing."""
    row_id = game_id or f"TEST_{game_date}"
    return build_prediction_row(
        game_date=game_date,
        game_id=row_id,
        home_team="HOME",
        away_team="AWAY",
        home_win=home_win,
        model_home_prob=model_prob,
        market_home_prob_no_vig=market_prob,
        market_away_prob_no_vig=round(1.0 - market_prob, 6),
    )


def _make_rows(
    n: int,
    *,
    model_prob: float = 0.55,
    market_prob: float = 0.52,
    start_year: int = 2025,
    home_win_pattern: str = "alternating",
) -> list[PredictionRow]:
    """
    Build n PredictionRows with sequential dates (2025-01-01 …).
    home_win_pattern: "alternating" | "all_home" | "all_away"
    """
    rows: list[PredictionRow] = []
    # Start from Jan 1 of start_year and advance by 1 day per row.
    # Using a simple day counter to avoid datetime import complexity.
    day = 1
    month = 1
    year = start_year
    for i in range(n):
        date_str = f"{year:04d}-{month:02d}-{day:02d}"
        if home_win_pattern == "alternating":
            hw = i % 2
        elif home_win_pattern == "all_home":
            hw = 1
        else:
            hw = 0
        rows.append(_make_row(date_str, model_prob, market_prob, hw,
                               game_id=f"T{i:04d}"))
        # Advance date (simplified, no month boundary logic needed)
        day += 1
        if day > 28:
            day = 1
            month += 1
            if month > 12:
                month = 1
                year += 1
    return rows


