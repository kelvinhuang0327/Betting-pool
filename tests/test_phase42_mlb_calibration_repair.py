class TestIdentityCalibrator:
    def test_returns_raw_probs_unchanged(self):
        probs = [0.4, 0.6, 0.55, 0.3]
        labels = [0, 1, 1, 0]
        result = _apply_identity(probs, labels, probs)
        assert result == probs

    def test_does_not_modify_original_list(self):
        probs = [0.5, 0.6]
        labels = [1, 0]
        result = _apply_identity(probs, labels, probs)
        result[0] = 0.999
        assert probs[0] == 0.5  # original unchanged


class TestBinwiseCalibrator:
    def test_fits_only_on_train_fold(self):
        """
        Verify that binwise calibration uses only train data.
        With train all-home-win, every bin rate should be 1.0 or
        fall back to global rate 1.0.
        """
        train_probs = [0.5, 0.55, 0.6, 0.45] * 10
        train_labels = [1] * 40   # all home wins in train
        test_probs = [0.5, 0.55, 0.4]
        result = _apply_binwise(train_probs, train_labels, test_probs)
        for p in result:
            assert p == pytest.approx(1.0, abs=1e-9), (
                "Binwise trained on all-home-win must produce rate ≈ 1.0"
            )

    def test_empty_bin_falls_back_to_global_rate(self):
        """
        Bin-wise should fall back to global train outcome rate
        for any bin that has no train samples.
        Test with test_probs in a range with no train coverage.
        """
        # Train covers [0.4, 0.6] only
        train_probs = [0.4 + i * 0.01 for i in range(21)]  # 0.40..0.60
        train_labels = [1] * 10 + [0] * 11
        global_rate = sum(train_labels) / len(train_labels)

        # Test prob in an empty bin (near 0.0 — no train samples there)
        test_probs = [0.02]
        result = _apply_binwise(train_probs, train_labels, test_probs)
        assert len(result) == 1
        # Should be close to global_rate (fallback)
        assert abs(result[0] - global_rate) < 0.15

    def test_calibrated_probs_within_unit_interval(self):
        """Binwise output probabilities must be in [0, 1]."""
        train_probs = [0.3, 0.5, 0.7, 0.4, 0.6] * 8
        train_labels = [0, 1, 1, 0, 1] * 8
        test_probs = [0.1, 0.3, 0.5, 0.7, 0.9]
        result = _apply_binwise(train_probs, train_labels, test_probs)
        for p in result:
            assert 0.0 <= p <= 1.0


