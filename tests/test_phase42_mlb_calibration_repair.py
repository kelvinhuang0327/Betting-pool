class TestTimeAwareSplits:
    """Tests for make_time_aware_splits."""

    def test_splits_never_leak_future_rows(self):
        """Test rows: all train row dates < all test row dates (no leakage)."""
        rows = _make_rows(120)
        splits = make_time_aware_splits(rows, n_splits=5)
        assert splits, "Expected at least one split"
        for train_rows, test_rows in splits:
            train_dates = {r.game_date for r in train_rows}
            test_dates = {r.game_date for r in test_rows}
            # Every test date must be strictly later than every train date
            max_train_date = max(train_dates)
            min_test_date = min(test_dates)
            assert min_test_date > max_train_date, (
                f"Data leakage detected: test_date={min_test_date} "
                f"<= max train_date={max_train_date}"
            )

    def test_returns_empty_list_if_insufficient_total(self):
        """With fewer than min_train_n + min_test_n rows, expect no splits."""
        rows = _make_rows(10)  # 10 rows, needs 50 minimum
        splits = make_time_aware_splits(rows, n_splits=5, min_train_n=30, min_test_n=20)
        assert splits == []

    def test_respects_min_train_and_test_sizes(self):
        """Each returned split must have train_n >= min_train_n and test_n >= min_test_n."""
        rows = _make_rows(200)
        splits = make_time_aware_splits(rows, n_splits=5, min_train_n=30, min_test_n=20)
        for train_rows, test_rows in splits:
            assert len(train_rows) >= 30
            assert len(test_rows) >= 20

    def test_train_window_is_expanding(self):
        """Later folds must have larger train sets (expanding window)."""
        rows = _make_rows(150)
        splits = make_time_aware_splits(rows, n_splits=4, min_train_n=20, min_test_n=10)
        train_sizes = [len(tr) for tr, _ in splits]
        assert len(train_sizes) >= 2
        for i in range(1, len(train_sizes)):
            assert train_sizes[i] > train_sizes[i - 1], (
                f"Train size at fold {i} ({train_sizes[i]}) not greater "
                f"than fold {i-1} ({train_sizes[i-1]})"
            )


