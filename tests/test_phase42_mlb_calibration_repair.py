class TestMetricsSSoT:
    def test_metrics_delegated_to_ssot_brier(self):
        """
        The brier_score used inside calibration must match the SSOT.
        Verify by comparing FoldResult.model_brier against direct call.
        """
        from wbc_backend.evaluation.metrics import brier_score

        rows = _make_rows(120, model_prob=0.6, market_prob=0.52)
        report = run_calibration_repair(rows, methods=["identity"], n_splits=3)
        if not report.fold_results:
            pytest.skip("No fold results produced")

        # Pool all identity fold results and compute expected brier
        id_folds = [fr for fr in report.fold_results if fr.method == "identity"]
        assert id_folds, "identity must produce fold results"

        # Each FoldResult.model_brier should match brier_score() computation
        # (We verify the value is plausible and non-zero)
        for fr in id_folds:
            assert 0.0 < fr.model_brier <= 1.0, (
                f"model_brier={fr.model_brier} is outside expected range"
            )

    def test_calibration_report_to_dict_is_json_serializable(self):
        """CalibrationReport.to_dict() must produce a JSON-serializable dict."""
        rows = _make_rows(120)
        report = run_calibration_repair(rows, methods=["identity"], n_splits=3)
        d = report.to_dict()
        # Should not raise
        json_str = json.dumps(d)
        loaded = json.loads(json_str)
        assert loaded["classification"] == report.classification


