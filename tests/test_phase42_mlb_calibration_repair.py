class TestSafetyConstraints:
    def test_no_production_model_modified(self):
        """
        run_calibration_repair must not write to any production model file.
        We verify the known production model paths are not touched.
        """
        import os
        import time

        # Record mtimes of key files before running calibration
        model_paths = [
            ROOT / "wbc_backend" / "models",
            ROOT / "models",
        ]
        before_mtimes: dict[str, float] = {}
        for mp in model_paths:
            if mp.exists():
                for f in mp.rglob("*"):
                    if f.is_file():
                        before_mtimes[str(f)] = f.stat().st_mtime

        rows = _make_rows(120)
        run_calibration_repair(rows, methods=["identity"], n_splits=3)

        # Check no files were modified
        for path_str, before_mtime in before_mtimes.items():
            after_mtime = Path(path_str).stat().st_mtime
            assert after_mtime == before_mtime, (
                f"Production model file was modified: {path_str}"
            )

    def test_no_external_api_call(self):
        """
        calibration.py must not import requests/urllib/httpx/openai.
        Check module source for these imports.
        """
        calib_file = ROOT / "wbc_backend" / "evaluation" / "calibration.py"
        assert calib_file.exists()
        source = calib_file.read_text(encoding="utf-8")
        forbidden = ["import requests", "import httpx", "import openai",
                     "import urllib.request", "from urllib.request"]
        for token in forbidden:
            assert token not in source, (
                f"calibration.py must not import '{token}' (no external API calls)"
            )

    def test_no_candidate_patch_creation(self):
        """
        run_calibration_repair must never create a CANDIDATE_PATCH file.
        Verify by running and checking no such file appears.
        """
        candidate_patch_paths = list(ROOT.rglob("CANDIDATE_PATCH*"))
        before_count = len(candidate_patch_paths)

        rows = _make_rows(120)
        run_calibration_repair(rows, methods=["identity", "binwise"], n_splits=3)

        after_paths = list(ROOT.rglob("CANDIDATE_PATCH*"))
        assert len(after_paths) == before_count, (
            "run_calibration_repair must NOT create CANDIDATE_PATCH files"
        )
